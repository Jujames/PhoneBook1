package Directory;

public class Contacts {

	private String first;
	private String last;
	private String phone;
	
	public Contacts(String first, String last, String phone) {
	   
		this.first = first;
		this.last = last;
		this.phone = phone;
		
		
	}

	public String getFirst() {
		return first;
	}

	public String getLast() {
		return last;
	}

	public String getPhone() {
		return phone;
	}

	

	
	
}


